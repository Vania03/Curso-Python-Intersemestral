import math

def suma(a,b):
	return a+b

a , b = 4 , 5
print(suma(a,b))
print(suma(3.4,6))

def resta(a,b):
	return a-b

a , b = 4 , 5
print(resta(a,b))
print(resta(3.4,6))

def multiplicacion(a,b):
	return a*b

a , b = 4 , 5
print(multiplicacion(a,b))
print(multiplicacion(3.4,6))

def potencia(a,b):
	return a**b

a , b = 4 , 5
print(potencia(a,b))
print(potencia(3.4,6))


def raizCuadrada(a):
	return math.sqrt(a)

a=16
print(raizCuadrada(a))
